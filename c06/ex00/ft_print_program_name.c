#include <unistd.h>

int	main(int agrc, char *agrv[])
{
	int	i;

	i = 0;
	if (agrc > 0)
	{
		while (agrv[0][i])
		{
			write(1, &agrv[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
